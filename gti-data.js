// Tier helper for legend / colour
const GTI_TIERS = [
  { min: 8.000, label: "Very high",  color: "#7a0d0d" },
  { min: 6.000, label: "High",        color: "#d6604d" },
  { min: 4.000, label: "Medium",      color: "#f6a200" },
  { min: 2.000, label: "Low",         color: "#f6e58d" },
  { min: 0.001, label: "Very low",    color: "#74c476" },
  { min: 0.000, label: "No impact",   color: "#1b6f3a" }
];

function gtiTier(score) {
  if (score == null) return null;
  return GTI_TIERS.find((t) => score >= t.min) || GTI_TIERS[GTI_TIERS.length - 1];
}
